<img src="/images/promo/stickers/1_12.png" class="alignleft"/>
<span>
<h3>Increasing Physical Fitness Activity</h3>
<p>
Fit for Green will draw more people into gymnasiums.  For years going to the gym has been all about burning calories, increasing cardio endurance, or bulking up in the weight room and many have grown rather bored with those things.  Fit for Green creates new goals for working out so it’s more about helping your teammates as you compete for power, helping your planet as you contribute to a renewable energy cause, and helping yourself as you find new reasons to get and stay physically fit.    
</p>
</span>
</div>

<div class="feature-block">
<img src="/images/promo/stickers/1_44.png" class="alignright"/>
<span>
<h3>Generating Substantial Amounts of Renewable Energy</h3>
<p>
Fit for Green will enable the generation of substantial renewable energy.  For years skeptics have criticized the idea of humans generating any amount of meaningful renewable energy and, at the individual contributor level, they are correct.  An athlete working out for an hour could only create about 350 watt-hours of power, about 1/3 of a kilowatt-hour, which costs about 11 cents in the US.  An average human is much less, about 100 watt-hours per hour.  Fit for Green changes the game from the individual to the 40 million plus gym members in the US, the more than 150 million active Facebook Users, and the 900,000 plus pieces of self propelled gym machines in the United States today.
</p>
</span>
</div>

<div class="feature-block">
<img src="/images/promo/stickers/1_15.png" class="alignleft"/>
<span>
<h3>Caring for Your Planet</h3>
<p>
Fit for Green will increase environmental awareness.  As people exercise and generate power it will not only be about them creating enough energy to light a bulb or charge an iPod but it will also be about realizing what energy is and how difficult it is to create it.  Just imagine, after a grueling 1 hour cardio workout you look on the screen and see 150 watt-hours generated.  Next you relate that work you did to something real, “I can light a 75 watt bulb for two hours”.  Finally, take the work you did and realize that each time you leave that 75 watt bulb burning needlessly the same burden is put on your planet mostly by burning fossil fuels.
</p>
</span>
</div>

<div class="feature-block">
<img src="/images/promo/stickers/1_47.png" class="alignright"/>
<span>
<h3>Sharing Your Results</h3>
<p>
Fit for Green will enable members to share information about their renewable energy contributions and the contributions of the groups or teams they belong to.  Depending on the member’s preferences, their primary interest may lie in watching their teams, their gyms, or maybe even the global Fit for Green results.  Other members may be more interested in their own personal results. Some members may opt to share their results with their team, their social networks or with other Fit for Green members.  It may also be the case that some members will chose to remain discrete and keep their results to themselves.  At Fit for Green we plan to accommodate all of these users through a set of carefully designed customization features.
</p>
</span>
</div>

<div class="feature-block">
<img src="/images/promo/stickers/1_46.png" class="alignleft"/>
<span>
<h3>Connecting with Your Friends</h3>
<p>
Fit for Green will encourage members to join forces with friends and family in order to make working out and creating energy fun.  For some friends it may be all about the competition and for others it may be more about cooperation around some renewable energy creation goal that they have set as a team.  In either case the friends become a new driver to help members get healthier and at the same time contribute to a united renewable energy cause / effort. 
</p>
</span>
</div>
